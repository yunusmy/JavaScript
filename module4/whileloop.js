/**
 * while loop 
 * 
 * 
 * initialization;
 * while(condition){
 *    //Loop body
 *    increment/decrement
 * }
 */

let count = 1;
while (count < 5) {
  console.log(count);
  count++;
}

let num = 1;
while (num <= 10) { //focus on the condition
   console.log(num);
  num++;
}

let num2 = 50;
while (num2 <= 100) {
  console.log(num2);
  num2++;
}

